print("Bienvenido a su calculadora")
print("Ingrese el primer numero")
numero_1 = int(input())
print("Usted ingreso el numero", numero_1, "Ahora ingrese el segundo")
numero_2 = int(input())
op_1 = numero_1 + numero_2
op_2 = numero_1 - numero_2
op_3 = numero_1 // numero_2
op_4 = numero_1 * numero_2
op_5 = numero_1 ** numero_2
print("Sus resultados son:", op_1, op_2, op_3, op_4, op_5)